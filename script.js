// Enhanced image fallback handling
document.addEventListener('DOMContentLoaded', () => {
    const profileImg = document.querySelector('.profile-img');
    const fallbackAvatar = document.querySelector('.fallback-avatar');
    
    if (profileImg && fallbackAvatar) {
        // Handle image load error
        profileImg.addEventListener('error', () => {
            profileImg.style.display = 'none';
            fallbackAvatar.style.display = 'flex';
            console.log('Profile image not found, showing fallback avatar');
        });
        
        // Handle successful image load
        profileImg.addEventListener('load', () => {
            fallbackAvatar.style.display = 'none';
            profileImg.style.display = 'block';
            console.log('Profile image loaded successfully');
        });
        
        // Check if image is already loaded or failed
        if (profileImg.complete) {
            if (profileImg.naturalHeight === 0) {
                // Image failed to load
                profileImg.style.display = 'none';
                fallbackAvatar.style.display = 'flex';
            } else {
                // Image loaded successfully
                fallbackAvatar.style.display = 'none';
            }
        }
    }
});

